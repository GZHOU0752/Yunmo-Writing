    private static Map<String, Object> genre(String id, String name,
                                              String writingBlueprint, String... forbiddenTerms) {
        Map<String, Object> g = new LinkedHashMap<>();
        g.put("id", id);
        g.put("name", name);
        Map<String, Object> config = new LinkedHashMap<>();
        config.put("writing_blueprint", writingBlueprint);
        config.put("forbidden_terms", forbiddenTerms.length == 1 && forbiddenTerms[0].isEmpty()
                ? List.of() : List.of(forbiddenTerms));
        g.put("genre_config", config);
        return g;
    }

    @GetMapping("/list")
    public Mono<List<Map<String, Object>>> list() {
        return Mono.just(GENRES);
    }

    @GetMapping("/{id}")
    public Mono<Map<String, Object>> get(@PathVariable String id) {
        return Mono.justOrEmpty(
                GENRES.stream().filter(g -> g.get("id").equals(id)).findFirst()
        );
    }
